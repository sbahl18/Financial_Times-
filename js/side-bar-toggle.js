$(document).ready(function(){
    $('.dropdown-nav').css('display', 'none');
              
    $('.drp-btn').on('click', function() {
      $('.dropdown-nav').toggle();
      $(this).find('i').toggleClass('fa-chevron-circle-down fa-chevron-circle-up drop-arrow-up');
    }); 
});
